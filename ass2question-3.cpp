//C++ Program to Find Size of int, float, double and char in Your System
#include<iostream>
using namespace std;
int main()
		{
			cout<<" THE SIZE OF FUNDAMENTAL DATA TYPE IS ----------" << endl;
			cout<< " \nthe size of char is = "<< sizeof(char) <<" bytes"<< endl;
			cout<< " \nthe size of int is = "<< sizeof(int) <<" bytes" << endl;
			cout<< " \nthe size of float is = "<< sizeof(float) <<" bytes" << endl;
			cout<< " \nthe size of double is = "<< sizeof(double) <<" bytes" << endl;
			return 0;
		}
