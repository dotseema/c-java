#include<iostream>
using namespace std;
int main()
		{
			int x=45;
			cout<<"\nthe pre-increment for x is = "<< ++x <<endl;
			cout<<"\nthe post-increment for x is = "<< x++ <<endl;
			cout<<"\nafter incrementing by 1 x is = "<<x+1<<endl;
			cout<<"\nthe pre-decrement for x is = "<< --x << endl;
			cout<<"\nthe post-decrement for x is = "<< x-- << endl;	
			cout<<"\nafter decrementing by 1 x is = "<<x-1<<endl;
			return 0;
		}
